    private void refreshCart(){
        DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
        model.setRowCount(0);
        double total = 0;

        for(CartItem c : cart){
            model.addRow(new Object[]{
                c.getId(),
                c.getName(),
                c.getPrice(),
                c.getQty(),
                c.getSubtotal()
            });
            total += c.getSubtotal();
        }
        txtTotal.setText(String.valueOf(total));
    }

