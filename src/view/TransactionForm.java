    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (cmbProduct.getSelectedItem() == null) return;

        try {
            String selectedItem = cmbProduct.getSelectedItem().toString();
            String[] parts = selectedItem.split(" - ");

            int id = Integer.parseInt(parts[0]); // ID elemen 1
            String name = parts[1];               // ➔ Nama elemen 2
            double price = Double.parseDouble(txtPrice.getText());
            int qty = Integer.parseInt(txtQty.getText());

            if (qty <= 0) {
                JOptionPane.showMessageDialog(this, "Quantity must be greater than 0!");
                return;
            }

            boolean existing = false;
            for (CartItem item : cart) {
                if (item.getId() == id) {
                    item.setQty(item.getQty() + qty);
                    existing = true;
                    break;
                }
            }

            if (!existing) {
                cart.add(new CartItem(id, name, price, qty));
            }

            refreshCart();
            txtQty.setText("1");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for quantity.");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int row = tblCart.getSelectedRow();
        if (row != -1) {
            cart.remove(row);
            refreshCart();
        } else {
            JOptionPane.showMessageDialog(this, "Please select an item in the cart table to remove.");
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

