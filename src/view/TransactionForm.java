    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        if (cart.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Your cart is empty!");
            return;
        }
        try {
            double total = Double.parseDouble(txtTotal.getText());
            double payment = Double.parseDouble(txtPayment.getText());
            
            if (payment < total) {
                JOptionPane.showMessageDialog(this, "Insufficient payment amount!");
                return;
            }
            
            double change = payment - total;
            txtChange.setText(String.valueOf(change));
            
            boolean saved = transactionDAO.saveTransaction(total, payment, change, cart);
            if (saved) {
                JOptionPane.showMessageDialog(this, "Transaction completed!\nChange: " + change);
                cart.clear();
                refreshCart();
                clearInputFields();
            } else {
                JOptionPane.showMessageDialog(this, "Database storage failed inside TransactionDAO!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid payment format.");
        }catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_btnProcessActionPerformed

    private void cmbProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProductActionPerformed
        if (cmbProduct.getSelectedItem() == null) return;
        
        try {
            String selectedItem = cmbProduct.getSelectedItem().toString();
            // Add "[0]" to extract just the ID string before the hyphen
            int productId = Integer.parseInt(selectedItem.split(" - ")[0]);

            Product p = productDAO.getById(productId);
            if (p != null) {
                txtPrice.setText(String.valueOf(p.getPrice()));
            }
        } catch (Exception e) {
        txtPrice.setText("0.0");
    }

    }//GEN-LAST:event_cmbProductActionPerformed

